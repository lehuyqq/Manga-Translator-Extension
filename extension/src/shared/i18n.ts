export type I18nKey = MessageKey;

export function isUiLanguage(value: unknown): value is UiLanguage {
  return typeof value === 'string' && UI_LANGUAGES.some((language) => language.code === value);
}

export function normalizeUiLanguage(value: unknown): UiLanguage {
  return isUiLanguage(value) ? value : 'en';
}

export function t(lang: UiLanguage | undefined, key: I18nKey, vars: Record<string, string | number> = {}): string {
  const safeLang = normalizeUiLanguage(lang);
  const template = MESSAGES[safeLang][key] ?? MESSAGES.en[key];
  return template.replace(/\{(\w+)\}/g, (match, name: string) => {
    const value = vars[name];
    return value === undefined ? match : String(value);
  });
}

export function languageLabel(lang: UiLanguage | undefined, language: string): string {
  const safeLang = normalizeUiLanguage(lang);
  return LANGUAGE_LABELS[safeLang][language] ?? LANGUAGE_LABELS.en[language] ?? language;
}
